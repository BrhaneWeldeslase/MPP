package lab3_2;

public class Apartment {
	
 private String Name;
 private String Type;
   Apartment(String Name,String Type)
   {
	   this.Name=Name;
	   this.Type=Type;
   }
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
public String getType() {
	return Type;
}
public void setType(String type) {
	Type = type;
}

 
}
